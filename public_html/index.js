function usoDeSwitch(val) {
    var respuesta = "";

    switch(val) {
        case 1:
            respuesta = "alpha";
            break;
        case 2:
            respuesta = "beta";
            break;
        case 3:
            respuesta = "gamma";
            break;
        case 4:
            respuesta = "delta";
            break;
        default:
            respuesta = "Por defecto";
    }

    return respuesta;
}

console.log(UsoDeSwitch(1));